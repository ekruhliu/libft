#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int nb;
	int minus;

	i = 0;
	nb = 0;
	minus = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == 45 || str[i] == 43)
	{
		if (str[i] == 45)
			minus++;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	if (minus == 1)
		return (-nb);
	return (nb);
}
