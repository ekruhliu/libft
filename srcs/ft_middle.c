#include "libft.h"

int	ft_middle(int a, int b, int c)
{
	if ((b >= c && b <= a) || (b >= a && b <= c))
		return (b);
	if ((a >= b && a <= c) || (a >= c && a <= b))
		return (a);
	if ((c >= a && c <= b) || (c >= b && c <= a))
		return (c);
	return (0);
}
